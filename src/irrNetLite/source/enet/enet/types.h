typedef unsigned int enet_uint32;      /**< unsigned 32-bit type */

typedef signed char enet_sint8;       /**< signed 8-bit type  */
typedef signed short enet_sint16;     /**< signed 16-bit type */
typedef signed int enet_sint32;      /**< signed 32-bit type */

#endif /* __ENET_TYPES_H__ */

